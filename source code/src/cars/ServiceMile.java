package cars;

public class ServiceMile {
	private int amile = 0;
	private int bmile = 0;
	private int cmile = 0;
	public int getAmile() {
		return amile;
	}
	public void setAmile(int amile) {
		this.amile = amile;
	}
	public int getBmile() {
		return bmile;
	}
	public void setBmile(int bmile) {
		this.bmile = bmile;
	}
	public int getCmile() {
		return cmile;
	}
	public void setCmile(int cmile) {
		this.cmile = cmile;
	}
	
	
}
